    public void deterMadre(String madre) {
        if (vacio == true) {
            JOptionPane.showMessageDialog(null, "Este es el primer animal de su lista");
            animales.add(new animal(identificacion, madre, estado, mes, ano, dia, genero));
            aceptar = 1;
        } else if (madre.equals("compra")) {
            JOptionPane.showMessageDialog(null, "Es un animal comprado, desconocemos a su madre y lo añadimos a la lista");
            animales.add(new animal(identificacion, madre, estado, mes, ano, dia, genero));
            index = animales.size();
            aceptar = 1;
        } else {
            index = animales.size();
            JOptionPane.showMessageDialog(null, "No es un animal comprado, tiene madre. Buscaré si la madre existe");
            for (int i = 0; i <= index; i++) {
                if (madre.equals(animales.get(i).getIdentificacion())) {
                    System.out.println("La madre existe");
                    System.out.println(animales.get(i).getIdentificacion());
                    animales.add(new animal(identificacion, madre, estado, mes, ano, dia, genero));
                    aceptar = 1;
                    return;
                } else if (index - 1 == i) {
                    JOptionPane.showMessageDialog(null, "El animal madre, identificado como '" + madre + "', no presenta registro alguno. Por favor, vuelva a intentar");
                    aceptar = 0;
                    return;
                }
            }

        }
    }

