    private void RegisAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisAnimalActionPerformed
        llenaAnim();
        identificacion = JOptionPane.showInputDialog("Ingrese la identificación del animal");
        estado = JOptionPane.showInputDialog("Ingrese el estado del animal (Ok, preñado, enfermo, muerto)");
        dia = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el dia de nacimiento"));
        mes = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el mes de nacimiento"));
        ano = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el año de nacimiento"));
        genero = JOptionPane.showInputDialog("Ingrese el género (F o M)");

        vacio = animales.isEmpty();
        do {
            madre = JOptionPane.showInputDialog("Ingrese la identificación de la madre del animal. Si el animal fue comprado, por favor escriba 'compra'");
            deterMadre(madre);
        } while (aceptar == 0);
        aceptar = 0;
        vacio = animales.isEmpty();
        if (vacio == false) {
            JOptionPane.showMessageDialog(null, "El animal: " + animales.get(animales.size() - 1).getIdentificacion() + " fué añadido");
        }
    }//GEN-LAST:event_RegisAnimalActionPerformed

    private void ConsulAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsulAnimalActionPerformed
        consultar = JOptionPane.showInputDialog("¿Qué animal desea consultar?");
        for (animal r : animales) {
            no = no + 1;

            if (r.getIdentificacion().equals(consultar)) {
                JOptionPane.showMessageDialog(null, "Este animal tiene una edad de " +r.calculoEdad()+ " años, es hijo/a de " + r.getMadre() + " y es de género " + r.getGenero());
                no = 0;
                break;
            } 

        }
        if (no == animales.size()) {
            JOptionPane.showMessageDialog(null, "Ese animal no existe");
        }


    }//GEN-LAST:event_ConsulAnimalActionPerformed

